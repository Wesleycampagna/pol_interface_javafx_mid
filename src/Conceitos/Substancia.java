package Conceitos;

public class Substancia extends Evidencia {

	private String tipoSubstancia;

	private double peso;

	private String embalagem;

	private int quantidadeUnitaria;

    public String getTipoSubstancia() {
        return tipoSubstancia;
    }

    public void setTipoSubstancia(String tipoSubstancia) {
        this.tipoSubstancia = tipoSubstancia;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public String getEmbalagem() {
        return embalagem;
    }

    public void setEmbalagem(String embalagem) {
        this.embalagem = embalagem;
    }

    public int getQuantidadeUnitaria() {
        return quantidadeUnitaria;
    }

    public void setQuantidadeUnitaria(int quantidadeUnitaria) {
        this.quantidadeUnitaria = quantidadeUnitaria;
    }
        
        

}
